namespace ConsoleApplication2
{
	//calcul library
	public class CalculLib
	{
		public static int Add(int a, int b)
		{
			return a + b;
		}

		public static int Sub(int a, int b)
		{
			return a - b;
		}

		public static int Divide(int a, int b)
		{
			return a / b;
		}

		public static int Multiply(int a, int b)
		{
			return a * b;
		}
	}
}
